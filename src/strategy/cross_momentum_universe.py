def _deduplicate(symbols: list[str]) -> list[str]:
    """순서 유지하며 중복 제거."""
    seen: set[str] = set()
    result: list[str] = []
    for s in symbols:
        if s not in seen:
            seen.add(s)
            result.append(s)
    return result


